findSmallest :: (Num a, Ord a) => [a] -> [a]
findSmallest xs = helper xs []
 where
    helper [] _ = []
    helper (x:xs) [] = -1 : helper xs [x]
    helper (x:xs) (s:ss)
     | s < x = s : helper xs (x:s:ss)
     | otherwise = helper (x:xs) ss